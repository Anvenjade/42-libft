#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	long	l;

	l = n;
	if (n < 0)
	{
		write (fd, "-", 1);
		l *= -1;
	}
	if (l > 9)
		ft_putnbr_fd(l / 10, fd);
	c = l % 10 + '0';
	write(fd, &c, 1);
}
